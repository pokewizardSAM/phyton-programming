import subprocess

def colorize(prin_txt):
    command = "lolcat -a -d 100 -F 10000000000000000000"
    process = subprocess.Popen(command, shell=True, text=True, stdin=subprocess.PIPE, stdout=subprocess.PIPE, stderr=subprocess.PIPE)
    stdout, stderr = process.communicate(input=prin_txt)
    print(stdout)
    print(stderr)

    
